function deactivate() {
    clearBusy();
    setMonitorRunning(false);
    try {
        const tmpDir = os.tmpdir();
        const files = fs.readdirSync(tmpDir);
        for (const f of files) {
            if (/^esp_(bld|cmd|chain|pip|install|req|spiffs|fatfs|littlefs|ota|ota_switch|ota_erase|ota_read|flash_fs)_\d+\.tmp$/.test(f)) {
                try { fs.unlinkSync(path.join(tmpDir, f)); } catch {}
            }
        }
    } catch {}
    const terms = getTerms();
    for (const t of Object.values(terms)) { try { t.dispose(); } catch {} }
    setTerms({});
    const panel = getPartitionPanel();
    if (panel) { try { panel.dispose(); } catch {} setPartitionPanel(null); }
    const oc = getOutputChannel();
    if (oc) { try { oc.dispose(); } catch {} setOutputChannel(null); }
}

