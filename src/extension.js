async function cmdEditProject() {
    if (checkBusy()) return;
    const root = getActiveRoot();
    if (!root) { warnNoProject(); return; }

    const mainCmake = path.join(root, 'main', 'CMakeLists.txt');
    const rootCmake = path.join(root, 'CMakeLists.txt');

    const items = [];
    if (fs.existsSync(mainCmake)) {
        items.push({ label: '$(file-code) Edit main/CMakeLists.txt', description: 'Component editor (SRCS, REQUIRES, ...)', value: 'main' });
    }
    if (fs.existsSync(rootCmake)) {
        items.push({ label: '$(root-folder-opened) Edit Root CMakeLists.txt', description: 'Root editor (project, EXCLUDE_COMPONENTS, ...)', value: 'root' });
    }

    if (items.length === 0) {
        vscode.window.showErrorMessage('ESP: No CMakeLists.txt found.');
        return;
    }

    if (items.length === 1) {
        // Only one option — open directly
        if (items[0].value === 'root') {
            CmakeEditor.cmdCmakeEditorRoot();
        } else {
            CmakeEditor.cmdCmakeEditorMain();
        }
        return;
    }

    const picked = await vscode.window.showQuickPick(items, {
        title: 'ESP CMake Editor — Which file to edit?',
        placeHolder: 'Select CMakeLists.txt to edit visually',
    });
    if (!picked) return;

    if (picked.value === 'root') {
        CmakeEditor.cmdCmakeEditorRoot();
    } else {
        CmakeEditor.cmdCmakeEditorMain();
    }
}

