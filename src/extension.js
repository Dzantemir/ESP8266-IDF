function parsePartitionCsvForFsParts(root, subtypes) {
    const csvFilename = getPartitionCsvFilename(root);
    const csvPath = path.join(root, csvFilename);
    if (!fs.existsSync(csvPath)) return [];

    const results = [];
    try {
        const csv = fs.readFileSync(csvPath, 'utf8');
        for (const line of csv.split('\n')) {
            const trimmed = line.trim();
            if (!trimmed || trimmed.startsWith('#')) continue;
            const parts = trimmed.split(',').map(s => s.trim());
            if (parts.length < 5) continue;
            const subtype = (parts[2] || '').toLowerCase();
            // Match: spiffs, fat, fatfs, littlefs
            if (subtypes.includes(subtype)) {
                results.push({ name: parts[0], type: parts[1], subtype, offset: parts[3], size: parts[4] });
            }
        }
    } catch {}
    return results;
}

function getEsptoolPath() {
    const idfPath = getValidIdfPath();
    if (!idfPath) return null;
    // esptool.py lives in the SDK components
    const candidates = [
        path.join(idfPath, 'components', 'esptool_py', 'esptool', 'esptool.py'),
        path.join(idfPath, 'tools', 'esptool.py'),
    ];
    for (const p of candidates) {
        if (fs.existsSync(p)) return p;
    }
    // Try to find it in espressif tools
    const espTools = path.join(os.homedir(), '.espressif');
    try {
        const esptoolDir = path.join(espTools, 'tools', 'esptool');
        if (fs.existsSync(esptoolDir)) {
            const versions = fs.readdirSync(esptoolDir).sort().reverse();
            for (const ver of versions) {
                const candidate = path.join(esptoolDir, ver, 'esptool.py');
                if (fs.existsSync(candidate)) return candidate;
            }
        }
    } catch {}
    return null;
}

async function cmdFlashFsImage(fsLabel, subtypes, binSuffix) {
    if (checkBusy()) return;

    // #37: Use a helper that ensures clearBusy() on every early return
    const _earlyReturn = () => { clearBusy(); };
    setBusy(`Flash ${fsLabel}`);

    try {
    const root = getActiveRoot();
    if (!root) { _earlyReturn(); warnNoProject(); return; }

    const pythonCmd = await Python.getPythonCmd(true);
    if (!pythonCmd) { _earlyReturn(); return; }

    const idfPath = getValidIdfPath();
    if (!idfPath) {
        _earlyReturn();
        vscode.window.showErrorMessage('ESP: IDF_PATH not set or invalid!');
        return;
    }

    // #24: Check tools (esptool etc.) before proceeding
    const toolsOk = await IdfRunner.checkToolsOrPrompt(idfPath, pythonCmd);
    if (!toolsOk) { _earlyReturn(); return; }

    // Check Python dependencies before running esptool
    if (!await Python.checkPythonDeps(idfPath, pythonCmd)) { _earlyReturn(); return; }

    // 1. Find matching partitions in CSV
    const fsParts = parsePartitionCsvForFsParts(root, subtypes);

    if (fsParts.length === 0) {
        _earlyReturn();
        const ans = await vscode.window.showErrorMessage(
            `ESP Flash ${fsLabel}: No ${fsLabel} partition found in partition table.\n` +
            'Add a partition with the correct SubType first.',
            'Open Partition Editor', 'Cancel'
        );
        if (ans === 'Open Partition Editor') {
            vscode.commands.executeCommand('esp.partitionEditor');
        }
        return;
    }

    // 2. Find .bin files in project root
    const buildDir = path.join(root, 'build');
    const searchDirs = [root];
    if (fs.existsSync(buildDir)) searchDirs.push(buildDir);

    let foundBins = [];
    for (const dir of searchDirs) {
        try {
            const files = fs.readdirSync(dir).filter(f => f.endsWith(binSuffix));
            for (const f of files) {
                foundBins.push(path.join(dir, f));
            }
        } catch {}
    }

    if (foundBins.length === 0) {
        const uris = await vscode.window.showOpenDialog({
            canSelectFiles: true,
            canSelectFolders: false,
            canSelectMany: false,
            title: `Flash ${fsLabel}: Select .bin file`,
            defaultUri: fs.existsSync(buildDir) ? vscode.Uri.file(buildDir) : vscode.Uri.file(root),
            filters: { 'Binary files': ['bin'], 'All files': ['*'] },
            openLabel: 'Select firmware',
        });
        if (!uris?.length) { _earlyReturn(); return; }
        foundBins = [uris[0].fsPath];
    }

    // 3. Pick partition (if multiple)
    let selectedPart;
    if (fsParts.length === 1) {
        selectedPart = fsParts[0];
    } else {
        const items = fsParts.map(p => ({
            label: `$(package) ${p.name}`,
            description: `${p.subtype} — offset ${p.offset}, size ${p.size}`,
            detail: `Partition for ${fsLabel} flash`,
            part: p,
        }));
        const picked = await vscode.window.showQuickPick(items, {
            title: `Flash ${fsLabel}: Select target partition`,
            placeHolder: 'Choose which partition to flash to',
        });
        if (!picked) { _earlyReturn(); return; }
        selectedPart = picked.part;
    }

    // 4. Pick bin file (if multiple)
    let selectedBin;
    if (foundBins.length === 1) {
        selectedBin = foundBins[0];
    } else {
        const binItems = foundBins.map(f => ({
            label: `$(file-binary) ${path.basename(f)}`,
            description: path.dirname(f).replace(root, '.'),
            detail: f,
            binPath: f,
        }));
        const picked = await vscode.window.showQuickPick(binItems, {
            title: `Flash ${fsLabel}: Select .bin file`,
            placeHolder: 'Choose which .bin file to flash',
        });
        if (!picked) { _earlyReturn(); return; }
        selectedBin = picked.binPath;
    }

    // 5. Get port
    let port = cfg('comPort');
    if (!port) {
        port = await Ports.cmdSelectPort();
        if (!port) { _earlyReturn(); return; }
    }

    // #46: Validate port for shell metacharacters
    if (port && !/^[a-zA-Z0-9./\\_-]+$/.test(port)) {
        _earlyReturn();
        vscode.window.showErrorMessage('ESP: Invalid port name! Shell metacharacters are not allowed.');
        return;
    }

    // #25: Verify port is still available
    if (port) {
        const portHolder = { port };
        const portOk = await Ports.confirmPortOrReselect(portHolder);
        if (!portOk) { _earlyReturn(); return; }
        port = portHolder.port;
        if (port !== cfg('comPort')) await setCfg('comPort', port);
    }

    // 6. Confirm
    const confirmed = await vscode.window.showWarningMessage(
        `Flash ${path.basename(selectedBin)} to ${fsLabel} partition "${selectedPart.name}" at offset ${selectedPart.offset} on port ${port}?`,
        { modal: true, detail: `This will overwrite the ${fsLabel} partition on the device.\nOffset: ${selectedPart.offset}\nPort: ${port}` },
        'Flash'
    );
    if (confirmed !== 'Flash') { _earlyReturn(); return; }

    // 7. Run esptool.py write_flash
    const esptoolPath = getEsptoolPath();
    if (!esptoolPath) {
        _earlyReturn();
        vscode.window.showErrorMessage('ESP: esptool.py not found! Check that the ESP8266_RTOS_SDK is installed correctly.');
        return;
    }
    const termName = `ESP › Flash ${fsLabel}`;
    const t = getTerm(termName);
    t.show(true);

    const pycmd = pythonCmd.replace(/^& /, '').replace(/^"|"$/g, '');

    // #22: Respect overrideFlashConfig — only include flash params in manual mode
    const overrideFlash = cfg('overrideFlashConfig');
    const beforeFlash = cfg('beforeFlashing') || 'default_reset';
    const afterFlash  = cfg('afterFlashing')  || 'hard_reset';
    // --before / --after are TOP-LEVEL esptool options (not write_flash subcommand
    // options). They MUST appear BEFORE the 'write_flash' subcommand on the CLI.
    const esptoolArgs = ['--port', port,
        '--before', beforeFlash, '--after', afterFlash];

    if (overrideFlash) {
        const baud = cfg('flashBaud') || 115200;
        const compressed = cfg('useCompressedUpload') ?? true;
        const flashMode = cfg('flashMode') || 'dio';
        const flashFreq = cfg('flashFreq') || '40m';
        const flashSize = cfg('flashSize') || '2MB';
        esptoolArgs.push('--baud', String(baud));
        esptoolArgs.push('write_flash',
            '--flash_mode', flashMode,
            '--flash_freq', flashFreq,
            '--flash_size', flashSize,
            compressed ? '--compress' : '--no-compress',
        );
    } else {
        // Menuconfig mode: let esptool read flash settings from binary image
        esptoolArgs.push('write_flash');
    }

    esptoolArgs.push(selectedPart.offset, q(selectedBin));

    const envPrefix = buildIdfEnvPrefix(idfPath, pythonCmd);
    const markerFile = path.join(os.tmpdir(), `esp_flash_fs_${Date.now()}.tmp`);

    if (IS_WIN) {
        const cmdParts = [
            `Set-Location ${q(root)}`,
            envPrefix,
            `& ${q(pycmd)} ${q(esptoolPath || 'esptool.py')} ${esptoolArgs.join(' ')}`,
        ];
        t.sendText(cmdParts.join('; ') + buildMarkerCmd(markerFile));
    } else {
        const cmdParts = [
            `cd ${q(root)}`,
            envPrefix,
            `${q(pycmd)} ${q(esptoolPath || 'esptool.py')} ${esptoolArgs.join(' ')}`,
        ];
        t.sendText(cmdParts.join(' && ') + buildMarkerCmd(markerFile));
    }

    log(`[Flash ${fsLabel}] writing ${selectedBin} to offset ${selectedPart.offset} on ${port} (overrideFlash=${overrideFlash})`);

    watchCommandDone(markerFile, termName).then(exitCode => {
        if (exitCode === 0) {
            vscode.window.showInformationMessage(`✅ Flash ${fsLabel}: ${path.basename(selectedBin)} written to "${selectedPart.name}" at ${selectedPart.offset}!`);
            log(`[Flash ${fsLabel}] ✅ OK`);
            // Auto-start monitor if configured
            const postAction = cfg('postFlashFsAction') || 'none';
            if (postAction === 'monitor') {
                log(`[Flash ${fsLabel}] starting monitor (postFlashFsAction=monitor)`);
                clearBusy(); // Must clear before runFlash — it checks checkBusy() first
                Flash.runFlash('monitor');
                return; // monitor will set its own busy state
            }
        } else if (exitCode > 0) {
            vscode.window.showErrorMessage(`❌ Flash ${fsLabel} failed (exit ${exitCode}). Check terminal for details.`);
            log(`[Flash ${fsLabel}] ❌ failed (exit ${exitCode})`);
        }
        clearBusy();
    }).catch(() => clearBusy());

    } catch (e) {
        clearBusy();
        log(`[Flash ${fsLabel}] error: ${e.message}`);
    }
}

/**
 * Detect filesystem type(s) from CMakeLists.txt.
 * Looks for the block:
 *   # ESP8266 Tools: partition bin links -- BEGIN
 *   esptool_py_flash_project_args(storage 0xB0000 "data_spiffs.bin" FLASH_IN_PROJECT)
 *   # ESP8266 Tools: partition bin links -- END
 * Returns array of { fsType:'spiffs'|'fatfs'|'littlefs', partitionName, offset, binPath } or null.
 * #38: Returns ALL found FS entries so caller can prompt user to choose.
 */
function detectFsTypeFromCmake(root) {
    const cmakePath = path.join(root, 'CMakeLists.txt');
    if (!fs.existsSync(cmakePath)) return null;

    try {
        const content = fs.readFileSync(cmakePath, 'utf8');
        const beginMarker = '# ESP8266 Tools: partition bin links -- BEGIN';
        const endMarker   = '# ESP8266 Tools: partition bin links -- END';
        const beginIdx = content.indexOf(beginMarker);
        const endIdx   = content.indexOf(endMarker);
        if (beginIdx < 0 || endIdx < 0 || endIdx <= beginIdx) return null;

        const block = content.substring(beginIdx, endIdx + endMarker.length);

        // Parse ALL esptool_py_flash_project_args entries (use matchAll for multiple FS partitions)
        const allMatches = [...block.matchAll(/esptool_py_flash_project_args\s*\(\s*(\S+)\s+(0x[0-9a-fA-F]+)\s+"([^"]+)"/g)];
        if (allMatches.length === 0) return null;

        // Find all FS-type entries
        const fsEntries = [];
        for (const m of allMatches) {
            const partitionName = m[1];
            const offset        = m[2];
            const binPath       = m[3];
            const binLower      = binPath.toLowerCase();

            let fsType = null;
            if (binLower.includes('spiffs'))     fsType = 'spiffs';
            else if (binLower.includes('littlefs')) fsType = 'littlefs';
            else if (binLower.includes('fatfs') || binLower.includes('fat')) fsType = 'fatfs';

            if (fsType) fsEntries.push({ fsType, partitionName, offset, binPath });
        }

        if (fsEntries.length === 0) return null;
        return fsEntries; // #38: return ALL FS entries
    } catch {
        return null;
    }
}

async function cmdFlashFileSystem() {
    if (checkBusy()) return;

    const root = getActiveRoot();
    if (!root) { warnNoProject(); return; }

    // 1. Try auto-detection from CMakeLists.txt
    // #38: detectFsTypeFromCmake now returns an array of all FS entries
    const cmakeEntries = detectFsTypeFromCmake(root);

    // Map of fsType → { label, subtypes, binSuffix }
    const FS_MAP = {
        spiffs:   { label: 'SPIFFS',   subtypes: ['spiffs'],           binSuffix: '_spiffs.bin'   },
        fatfs:    { label: 'FATFS',     subtypes: ['fat', 'fatfs'],      binSuffix: '_fatfs.bin'    },
        littlefs: { label: 'LittleFS',  subtypes: ['littlefs'],          binSuffix: '_littlefs.bin' },
    };

    if (cmakeEntries && cmakeEntries.length > 0) {
        if (cmakeEntries.length === 1) {
            // Single FS partition — auto-detect straightforward
            const entry = cmakeEntries[0];
            const fsInfo = FS_MAP[entry.fsType];
            const autoDetect = await vscode.window.showInformationMessage(
                `ESP: Auto-detected filesystem: ${fsInfo.label} (from CMakeLists.txt)`,
                'Continue', 'Choose manually'
            );
            if (autoDetect === 'Choose manually') {
                // Fall through to manual selection
            } else if (autoDetect === 'Continue') {
                return cmdFlashFsImage(fsInfo.label, fsInfo.subtypes, fsInfo.binSuffix);
            } else {
                return; // cancelled
            }
        } else {
            // #38: Multiple FS partitions — let user choose which one
            const pickItems = cmakeEntries.map(e => {
                const fsInfo = FS_MAP[e.fsType];
                return {
                    label: `$(cloud-upload) ${fsInfo.label}`,
                    description: `${e.partitionName} — offset ${e.offset}`,
                    detail: `Auto-detected from CMakeLists.txt: ${e.binPath}`,
                    value: e.fsType,
                };
            });
            pickItems.push({
                label: '$(list-unordered) Choose manually...',
                description: 'Select filesystem type manually',
                detail: 'Skip auto-detection',
                value: '_manual',
            });
            const picked = await vscode.window.showQuickPick(pickItems, {
                title: 'Flash File System: Multiple partitions detected',
                placeHolder: 'Select which filesystem to flash',
            });
            if (!picked) return;
            if (picked.value === '_manual') {
                // Fall through to manual selection
            } else {
                const fsInfo = FS_MAP[picked.value];
                return cmdFlashFsImage(fsInfo.label, fsInfo.subtypes, fsInfo.binSuffix);
            }
        }
    }

    // 2. Manual selection (or auto-detect failed)
    // #49: Check partition table first — only show FS types that the project actually uses
    const availableFsTypes = [];
    for (const [key, val] of Object.entries(FS_MAP)) {
        const parts = parsePartitionCsvForFsParts(root, val.subtypes);
        if (parts.length > 0) {
            availableFsTypes.push({ key, val, parts });
        }
    }

    if (availableFsTypes.length === 0) {
        const ans = await vscode.window.showErrorMessage(
            'ESP Flash File System: No filesystem partitions found in partition table.\n' +
            'Add a filesystem partition (SPIFFS, FATFS, or LittleFS) first.',
            'Open Partition Editor', 'Cancel'
        );
        if (ans === 'Open Partition Editor') {
            vscode.commands.executeCommand('esp.partitionEditor');
        }
        return;
    }

    if (availableFsTypes.length === 1) {
        // Only one FS type has partitions — flash it directly, no need to ask
        const { val: fsInfo } = availableFsTypes[0];
        return cmdFlashFsImage(fsInfo.label, fsInfo.subtypes, fsInfo.binSuffix);
    }

    // Multiple FS types have partitions — let user choose which one
    const items = availableFsTypes.map(({ key, val, parts }) => ({
        label: `$(cloud-upload) ${val.label}`,
        description: `Flash ${val.label} .bin to partition`,
        detail: `${parts.length} partition(s): ${parts.map(p => p.name).join(', ')}`,
        value: key,
    }));

    const picked = await vscode.window.showQuickPick(items, {
        title: 'Flash File System: Select filesystem type',
        placeHolder: 'Choose filesystem type',
    });
    if (!picked) return;

    const fsInfo = FS_MAP[picked.value];
    return cmdFlashFsImage(fsInfo.label, fsInfo.subtypes, fsInfo.binSuffix);
}

async function cmdResetConfig() {
    if (checkBusy()) return;
    if (!await Python.requireReady()) return;
    const root = getActiveRoot();
    if (!root) { warnNoProject(); return; }

    const sdkconfig    = path.join(root, 'sdkconfig');
    const sdkconfigOld = path.join(root, 'sdkconfig.old');

    const exists    = fs.existsSync(sdkconfig);
    const existsOld = fs.existsSync(sdkconfigOld);

    if (!exists && !existsOld) {
        vscode.window.showInformationMessage('ESP: No sdkconfig files found — nothing to reset.');
        return;
    }

    const files = [exists && 'sdkconfig', existsOld && 'sdkconfig.old'].filter(Boolean).join(' + ');
    const choice = await vscode.window.showWarningMessage(
        `ESP: Delete ${files}? All menuconfig settings will be reset to defaults on next build.`,
        { modal: true },
        'Delete', 'Cancel'
    );
    if (choice !== 'Delete') return;

    let deleted = [];
    try { if (exists)    { fs.unlinkSync(sdkconfig);    deleted.push('sdkconfig'); }    } catch (e) { vscode.window.showErrorMessage(`ESP: Failed to delete sdkconfig: ${e.message}`); return; }
    try { if (existsOld) { fs.unlinkSync(sdkconfigOld); deleted.push('sdkconfig.old'); } } catch (e) { log(`Failed to delete sdkconfig.old: ${e.message}`); vscode.window.showWarningMessage(`ESP: Could not delete sdkconfig.old: ${e.message}`); }

    // #27: Invalidate sdkconfig cache so stale values are not returned
    setSdkconfigCache(null);

    vscode.window.showInformationMessage(`ESP: Deleted ${deleted.join(' + ')}. Run Build to regenerate with defaults.`);
    log(`Reset Config: deleted ${deleted.join(', ')}`);
}

